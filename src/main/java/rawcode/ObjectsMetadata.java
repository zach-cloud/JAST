package rawcode;

public class ObjectsMetadata {

    private int numModifiedObjects;
    private int numNewObjects;

    public int getNumModifiedObjects() {
        return numModifiedObjects;
    }

    public void setNumModifiedObjects(int numModifiedObjects) {
        this.numModifiedObjects = numModifiedObjects;
    }

    public int getNumNewObjects() {
        return numNewObjects;
    }

    public void setNumNewObjects(int numNewObjects) {
        this.numNewObjects = numNewObjects;
    }
}
